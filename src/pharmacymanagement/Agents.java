    /**
     * This method Deletes Agent data from the database and show data into Agents Table.
    */
    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        // TODO add your handling code here:
        if(Aid.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Enter Agent ID to be Deleted");
        }
        else
        {
            try
            {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy", "root", "");
                String Id = Aid.getText();
                String Query = "Delete from agenttbl where Agent_Id="+Id;
                Statement Del = Con.createStatement();
                Del.executeUpdate(Query);
                SelectAgnt();
                JOptionPane.showMessageDialog(this, "Agent Deleted Successfully");
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked

