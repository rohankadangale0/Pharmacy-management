    /**
    * This method Update the data into AgentTable in database and show data into Agents Table.
    * <p>
    * Before Updating the values it checks if the Update Button is enable, 
    * if the Button is enable then it checks is Agent Name, Id, Age, Password 
    * is empty, if it is empty it display error message to user and everything 
    * is okay then it allows to Update the data, after Updating it 
    * display the Success Message.
    * </p> 
    */
    private void UpdateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateBtnMouseClicked

        if(enable == true)
        {
            UpdateBtn.setEnabled(true);
        }

        if(Aid.getText().isEmpty() || Aname.getText().isEmpty() || Aage.getText().isEmpty() || Apass.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Missing Agent Information");
        }
        else
        {
            try
            {

                Class.forName("com.mysql.cj.jdbc.Driver");
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy", "root", "");
                String Id = Aid.getText();
                
                String UpdateQuery = "Update agenttbl set Agent_Name= '"+Aname.getText()+"'"+","
                + "Agent_Age = '"+Aage.getText()+"'"+","+ "Agent_Phone = '"+Aphone.getText()+"'"+","
                + "Agent_Password = '"+Apass.getText()+"'"+","
                + "Gender = '"+GenderCb.getSelectedItem().toString()+"'"+"where Agent_Id = "+Id;
                
                Statement updt = Con.createStatement();
                updt.executeUpdate(UpdateQuery);
                SelectAgnt();
                JOptionPane.showMessageDialog(this, "AgentTable Updated Successfully");
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_UpdateBtnMouseClicked

