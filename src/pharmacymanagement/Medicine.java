    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
        // TODO add your handling code here:

        ManfactDate = ManuFact_Date.getDate();
        MyManfact = new java.sql.Date(ManfactDate.getTime());
        ExpDate = Expry_Date.getDate();
        MyExp = new java.sql.Date(ExpDate.getTime());
       
        try
        {
            if(MyExp.compareTo(MyManfact) < 0)
            {
                JOptionPane.showMessageDialog(this, "Expiry Date Cannot be Less then Manufacturing Date. Please Re-Enter the Dates Properly.");
            }
            else
            {
                
                
                
                Class.forName("com.mysql.cj.jdbc.Driver");
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy", "root", "");
                
                
                PreparedStatement add = Con.prepareStatement("Insert Into medicinetbl values(?,?,?,?,?,?,?)");

                add.setInt(1, Integer.valueOf(Med_Id.getText()));
                add.setString(2, MedName.getText());
                add.setInt(3, Integer.valueOf(MedPrice.getText()));
                add.setInt(4, Integer.valueOf(MedQty.getText()));
                add.setDate(5, MyManfact);
                add.setDate(6, MyExp);
                add.setString(7, Med_Company.getSelectedItem().toString());

                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Medicine Successfully Added");

                Con.close();

                //to display entered records in GUI table 
                SelectMed();

                Med_Id.setText("");
                MedName.setText("");
                MedQty.setText("");
                MedPrice.setText("");
            }
       
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_AddBtnMouseClicked
    /**
     * This method Deletes Medicine data from the database and show data into Medicine Table.
    */
    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        // TODO add your handling code here:
        if(Med_Id.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Enter Medicines ID to be Deleted");
        }
        else
        {
            try
            {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy", "root", "");
                String Id = Med_Id.getText();
                String Query = "Delete from medicinetbl where Id="+Id;
                Statement Del = Con.createStatement();
                Del.executeUpdate(Query);
                SelectMed();
                JOptionPane.showMessageDialog(this, "Medicines Deleted Successfully");
            }
            catch(Exception e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked
    /**
     * This method Displays the data from the database into Medicine Table.
    */
    private void MedicineTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MedicineTableMouseClicked
        // TODO add your handling code here:
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel)MedicineTable.getModel();
        int Myindex = MedicineTable.getSelectedRow();
        
        Med_Id.setText(model.getValueAt(Myindex, 0).toString());
        MedName.setText(model.getValueAt(Myindex, 1).toString());
        MedPrice.setText(model.getValueAt(Myindex, 2).toString());
        MedQty.setText(model.getValueAt(Myindex, 3).toString());
        
        Med_Id.setEditable(false);
        UpdateBtn.setEnabled(true);
    }//GEN-LAST:event_MedicineTableMouseClicked
    
    /**
    * This method Update the data into MedicineTable in database.
    * <p>
    * Before Updating the values it checks if the Update Button is enable, 
    * if the Button is enable then it checks is Medicine Name, Id, Qty, Price 
    * is empty, if it is empty it display error message to user and everything 
    * is okay then it allows to Update the data, after Updating it 
    * display the Success Message.
    * </p> 
    */

    private void UpdateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateBtnMouseClicked
        // TODO add your handling code here:
        
        
       
        if(Med_Id.getText().isEmpty() || MedName.getText().isEmpty() || MedPrice.getText().isEmpty() || MedQty.getText().isEmpty())
        {
           JOptionPane.showMessageDialog(this, "Missing Medicine Information");
        }
        else
        {
            ManfactDate = ManuFact_Date.getDate();
            MyManfact = new java.sql.Date(ManfactDate.getTime());
            ExpDate = Expry_Date.getDate();
            MyExp = new java.sql.Date(ExpDate.getTime());
            
            
            try
            {
                if(MyExp.compareTo(MyManfact) < 0)
                {
                    JOptionPane.showMessageDialog(this, "Expiry Date Cannot be Less then Manufacturing Date. Please Re-Enter the Dates Properly.");
                }
                else
                {
                    
                    JOptionPane.showMessageDialog(this, "Medicine Id: " + Med_Id.getText() + "\n Medicine Name: " + MedName.getText() + 
                    "\n Medicine Price: "+ MedPrice.getText() + "\n Medicine Quantity: " + MedQty.getText() + 
                    "\n Manufacture: " + ManfactDate + "\n Expiry: " + ManfactDate);

                    Class.forName("com.mysql.cj.jdbc.Driver");
                    Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy", "root", "");
                    String Id = Med_Id.getText();
                    String UpdateQuery = "Update medicinetbl set Name= '"+MedName.getText()+"'"+","
                            + "Price = "+MedPrice.getText()+""+",Quantity = "+MedQty.getText()+""+","
                            + "Manufacture = '"+MyManfact+"'"+",Expiry = '"+MyExp+"'"+","
                            + "Company = '"+Med_Company.getSelectedItem().toString()+"'"+"where Id = "+Med_Id.getText();
                    Statement updt = Con.createStatement();
                    updt.executeUpdate(UpdateQuery);
                    SelectMed();
                    JOptionPane.showMessageDialog(this, "Medicines Updated Successfully");
                    
                    
       
                }

            }

            catch(Exception e)
            {
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_UpdateBtnMouseClicked
