    /**
     * This method retrieve the data from database and display into Medicine table.
    */
    
    public void Get_Latest_Id()
    {
        int latest_id = 0;
        String LatestID = "";
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy", "root", "");
            St = Con.createStatement();
            String query = "Select * from medicinetbl ORDER BY Id DESC LIMIT 1";
            Rs = St.executeQuery(query);

            while(Rs.next())
            {
                LatestID = Rs.getString("ID");
                JOptionPane.showMessageDialog(this, "Latest Id: " + LatestID);

                latest_id = Integer.parseInt(LatestID);
                latest_id ++;

                LatestID = Integer.toString(latest_id);
                JOptionPane.showMessageDialog(this, "Latest Id: " + LatestID);
            }
            
            Med_Id.setText(LatestID);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
    
    
    
